#include <iostream>

int main()
{
    // q: ここでは何していますか
    // a: ここでは、ポインタの減算をしています。
    char v[6];
    char* p = &v[3];
    p -= 3;
    char x = *p;

    // q: このコードは何を出力しますか
    // a: このコードは、0を出力します。
    // q: なぜですか
    // a: なぜなら、ポインタの減算は、ポインタの指すオブジェクトの型のサイズの倍数である必要があるからです。
    //    この場合、ポインタpはchar型のポインタなので、ポインタの減算はchar型のサイズの倍数である必要があります。
    //    しかし、ポインタpは、char型の配列vの3番目の要素を指しています。
    //    このため、ポインタpは、char型の配列vの3番目の要素からchar型の配列vの0番目の要素までのサイズの倍数である必要があります。
    //    しかし、char型の配列vの3番目の要素からchar型の配列vの0番目の要素までのサイズは、0です。
    //    このため、ポインタpは、char型の配列vの3番目の要素からchar型の配列vの0番目の要素までのサイズの倍数である必要がありますが、
    //    そのサイズは0であるため、ポインタpは、char型の配列vの3番目の要素からchar型の配列vの0番目の要素までのサイズの倍数ではありません。
    //    このため、ポインタpは、char型の配列vの3番目の要素からchar型の配列vの0番目の要素までのサイズの倍数でないため、
    //    ポインタの減算は、ポインタの指すオブジェクトの型のサイズの倍数である必要があるため、ポインタの減算はできません。
    std::cout << x << std::endl;
}
